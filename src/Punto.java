

public class Punto {
	private int px;
	private int py;
	
	
	
	public Punto(){

	}
	public Punto(int px, int py) {
		this.px = px;
		this.py = py;
	}
	public int getPx() {
		return px;
	}
	public int getPy() {
		return py;
	}
	public void setPx(int px) {
		this.px = px;
	}
	public void setPy(int py) {
		this.py = py;
	}
}
